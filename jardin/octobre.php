			<p> L’arrivée de l’automne est synonyme de grand nettoyage de printemps (étonnamment !). Semer de l’engrais vert à la volée pour préparer ses semis du printemps est une bonne habitude à prendre. En effet, vos prochaines cultures auront besoin de nutriments pour bien se développer. Les engrais verts (le trèfle, la moutarde, le sarrasin ou le seigle), ont la capacité d’améliorer la composition du sol (tout comme le compost). Ils sont également riches en nutriments, notamment en azote. Il faut les semer à l’automne ou au printemps puis les faucher dès qu’ils fleurissent pour les enfouir dans le sol. Ils vont alors se décomposer et libérer leurs propriétés fertilisantes. C’est donc la période idéale pour celles et ceux qui souhaitent se lancer dans l’aventure du jardinage. C’est le moment parfait pour préparer votre terre pour le printemps </p>
