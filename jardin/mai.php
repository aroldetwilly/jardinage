			<h1>Potimarron</h1>
			<p>Les potimarrons sont un groupe de cultivars eurasiatiques du potiron, une plante d'origine mésoaméricaine arrivée dans l'Ancien Monde lors de l'échange colombien. Ils auraient été introduits au Japon par des navigateurs portugais, et malgré leur implantation actuelle dans la gastronomie française, ils n'auraient gagné l'Europe depuis le Japon que tardivement1 (une famille japonaise aurait apporté ce légume en France en 1957 seulement). Ces premiers potimarrons, appelés alors « potiron doux de Hokkaido » se sont rapidement et largement diffusés dans la jardins d'Europe. C'est à partir de ce dernier que se développe la variété appelée « Potimarron français » qui est aujourd'hui une des plus cultivées dans les jardins potagers avec les variétés japonaises « Uchiki kuri », « Akaguri » et « Red kuri ».<br/>Comme les potirons, les potimarrons peuvent être consommés en potage, au four avec de l'ail, frits, en tourte ou en purée. À la différence du potiron classique, il n'est pas nécessaire de retirer la peau du potimarron avant la cuisson. Il peut aussi être consommé cru. Les graines peuvent être conservées et grillées pour l'apéritif2.</p>		
